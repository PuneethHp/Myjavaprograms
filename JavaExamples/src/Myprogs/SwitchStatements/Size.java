package Myprogs.SwitchStatements;

public class Size {
    public static void main(String[] args) {
        int number = 3;
        String size;
        switch (number){
            case 1:
                size = "Small";
                break;
            case 2:
                size = "Medium";
                break;
            case 3:
                size = "Large";
                break;
            default:
                size = "Unknown";
                break;}
        System.out.println("size:"+size);}


        }


