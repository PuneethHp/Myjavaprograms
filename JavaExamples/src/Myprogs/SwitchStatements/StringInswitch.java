package Myprogs.SwitchStatements;

public class StringInswitch {
    public static void main(String[] args) {
        String name = "Limka";
        switch(name){
            case "Mango":
                System.out.println("It is a fruit");
                break;
            case "Tomato":
                System.out.println("It is a vegetable");
                break;
            case "Limka":
                System.out.println("It is cold drink");
        }
    }
}


