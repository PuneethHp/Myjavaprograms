package Myprogs.Loops;

public class PrintingNumber {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            switch (i) {
                case 2:
                    System.out.println(i);
                    break;
                case 3:
                    System.out.println(i);
                    break;
                case 4:
                    System.out.println(i);
                    break;
                case 5:
                    System.out.println(i);
                    break;
                default:
                    System.out.println("number not available");
                    break;
            }
        }
    }
}
